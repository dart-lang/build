/// Builds an asset.
typedef AssetBuilder = Future<void> Function(AssetId);

/// Whether an asset is a "processed output".
///
/// If so, there is no need to build it.
typedef AssetIsProcessedOutput = bool Function(AssetId);

/// Evaluates all assets matching a glob.
typedef GlobEvaluator = Future<void> Function(GlobId);

/// Describes if and how a [SingleStepReaderWriter] should read an [AssetId].
class Readability {
  final bool canRead;
  final bool inSamePhase;

  const Readability({required this.canRead, required this.inSamePhase});

  /// Determines readability for an output written in a previous build phase,
  /// which means that [ownOutput] is impossible.
  factory Readability.fromPreviousPhase(bool readable) =>
      readable ? Readability.readable : Readability.notReadable;

  static const Readability notReadable = Readability(
    canRead: false,
    inSamePhase: false,
  );
  static const Readability readable = Readability(
    canRead: true,
    inSamePhase: false,
  );
  static const Readability ownOutput = Readability(
    canRead: true,
    inSamePhase: true,
  );
}

/// `SingleStepReaderWriter`'s view on the currently-running build.
class RunningBuild {
  final BuildPackages buildPackages;
  final BuildConfigs buildConfigs;
  final BuildState buildState;
  final AssetBuilder assetBuilder;
  final AssetIsProcessedOutput assetIsProcessedOutput;
  final GlobEvaluator globEvaluator;

  RunningBuild({
    required this.buildPackages,
    required this.buildConfigs,
    required this.buildState,
    required this.assetBuilder,
    required this.assetIsProcessedOutput,
    required this.globEvaluator,
  });
}

/// `SingleStepReaderWriter`'s view on the currently-running build step.
class RunningBuildStep {
  final int phaseNumber;
  final BuildPhase buildPhase;
  final String primaryPackage;

  RunningBuildStep({
    required this.phaseNumber,
    required this.buildPhase,
    required this.primaryPackage,
  });
}

/// File operations for a single step in a build.
///
/// A step is a single Builder and primary input (or package for package
/// builders) combination.
///
/// Limits reads to the assets which are sources or were generated by previous
/// phases.
///
/// Tracks the assets and globs read during this step for input dependency
/// tracking.
class SingleStepReaderWriter implements PhasedReader {
  late final AssetFinder assetFinder = FunctionAssetFinder(_findAssets);

  final RunningBuild? _runningBuild;
  final RunningBuildStep? _runningBuildStep;

  final ReaderWriter _delegate;

  final InputTracker inputTracker;

  /// The assets written via [writeAsString] or [writeAsBytes].
  final Set<AssetId> assetsWritten;

  final partsWritten = <String>[];
  void writePart(String content) {
    partsWritten.add(content);
  }

  SingleStepReaderWriter({
    required RunningBuild? runningBuild,
    required RunningBuildStep? runningBuildStep,
    required ReaderWriter readerWriter,
    required this.inputTracker,
    required this.assetsWritten,
  }) : _runningBuild = runningBuild,
       _runningBuildStep = runningBuildStep,
       _delegate = readerWriter {
    if (runningBuildStep != null) {
      if (runningBuild == null) {
        throw ArgumentError(
          '`runningBuildStep` was set without `runningBuild`, they must`'
          'be both null or both set.',
        );
      }
    }
    if (runningBuildStep == null) {
      if (runningBuild != null) {
        throw ArgumentError(
          '`runningBuildStep` was not set but `runningBuild` '
          'was, they must be both null or both set.',
        );
      }
    }
  }

  factory SingleStepReaderWriter.fakeFor(ReaderWriter readerWriter) {
    return SingleStepReaderWriter(
      runningBuild: null,
      runningBuildStep: null,
      readerWriter: readerWriter,
      inputTracker: InputTracker(readerWriter.filesystem),
      assetsWritten: {},
    );
  }

  @override
  int get phase => _runningBuildStep?.phaseNumber ?? 0;

  /// Checks whether [id] can be read by this step - attempting to build the
  /// asset if necessary.
  ///
  /// If [catchInvalidInputs] is set to true and [_checkInvalidInput] throws an
  /// [InvalidInputException], this method will return `false` instead of
  /// throwing.
  Future<bool> _isReadable(
    AssetId id, {
    bool catchInvalidInputs = false,
    bool track = true,
  }) async {
    try {
      _checkInvalidInput(id);
    } on InvalidInputException {
      if (catchInvalidInputs) return false;
      rethrow;
    } on PackageNotFoundException {
      if (catchInvalidInputs) return false;
      rethrow;
    }

    if (_runningBuild == null) {
      if (track) inputTracker.add(id);
      return _delegate.canRead(id);
    }

    final buildState = _runningBuild.buildState;
    if (buildState.isPlaceholder(id)) return false;
    if (!buildState.isFile(id)) {
      if (track) inputTracker.add(id);
      buildState.addMissingSource(id);
      return false;
    }

    final readability = await _isReadableId(id);

    // If it's in the same phase it's never an input: it is either an output of
    // the current generator, which means it's readable but not an input, or
    // it's an output of a generator running in parallel, which means it's
    // hidden and can't be an input.
    if (!readability.inSamePhase) {
      if (track) inputTracker.add(id);
    }

    return readability.canRead;
  }

  Future<bool> canRead(AssetId id, {bool track = true}) async {
    if (_runningBuild != null && _isGpAsset(id)) {
      if (track) inputTracker.add(id);
      return _readGpContent(id, upToPhase: phase) != null;
    }

    final isReadable = await _isReadable(
      id,
      catchInvalidInputs: true,
      track: track,
    );
    if (!isReadable) return false;
    if (_runningBuild == null) return true;

    if (_runningBuild.buildState.isDeclaredOutput(id) &&
        !await _delegate.canRead(id)) {
      return false;
    }

    await _ensureDigest(id);
    return true;
  }

  Future<Digest> digest(AssetId id, {bool track = true}) async {
    if (_runningBuild != null && _isGpAsset(id)) {
      if (track) inputTracker.add(id);
      final content = _readGpContent(id, upToPhase: phase);
      if (content == null) {
        throw AssetNotFoundException(id);
      }
      return md5.convert(utf8.encode(content));
    }

    final isReadable = await _isReadable(id, track: track);

    if (!isReadable) {
      throw AssetNotFoundException(id);
    }
    return _ensureDigest(id);
  }

  Future<List<int>> readAsBytes(AssetId id) async {
    if (_runningBuild != null && _isGpAsset(id)) {
      inputTracker.add(id);
      final content = _readGpContent(id, upToPhase: phase);
      if (content == null) {
        throw AssetNotFoundException(id);
      }
      return utf8.encode(content);
    }

    final isReadable = await _isReadable(id);
    if (!isReadable) {
      throw AssetNotFoundException(id);
    }
    await _ensureDigest(id);
    return _delegate.readAsBytes(id);
  }

  Future<String> readAsString(
    AssetId id, {
    Encoding encoding = utf8,
    bool track = true,
  }) async {
    if (_runningBuild != null && _isGpAsset(id)) {
      if (track) inputTracker.add(id);
      final content = _readGpContent(id, upToPhase: phase);
      if (content == null) {
        throw AssetNotFoundException(id);
      }
      return content;
    }

    final isReadable = await _isReadable(id, track: track);
    if (!isReadable) {
      throw AssetNotFoundException(id);
    }
    await _ensureDigest(id);
    return _delegate.readAsString(id, encoding: encoding);
  }

  Stream<AssetId> _findAssets(Glob glob, {required String package}) {
    if (_runningBuild == null) {
      return _delegate.assetFinder.find(glob, package: package);
    }

    final streamCompleter = StreamCompleter<AssetId>();

    _evaluateGlob(glob.pattern).then((globId) {
      inputTracker.addGlob(globId);
      final globResult = _runningBuild.buildState.globResultFor(globId)!;
      streamCompleter.setSourceStream(Stream.fromIterable(globResult.results));
    });
    return streamCompleter.stream;
  }

  /// Returns the `lastKnownDigest` of [id], computing and caching it if
  /// necessary.
  ///
  /// This is called on any read or existence check. With filesystem caching
  /// this ensures that the cached file content matches the build state hash.
  ///
  /// Note that [id] must exist in the build state.
  Future<Digest> _ensureDigest(AssetId id) async {
    if (_runningBuild == null) return _delegate.digest(id);
    final knownDigest = _runningBuild.buildState.digestOf(id);
    if (knownDigest != null) return knownDigest;

    Digest digest;
    try {
      digest = await _delegate.digest(id);
    } on AssetNotFoundException {
      await ChildProcess.exitDueToAssetDeleted(id);
    }
    _runningBuild.buildState.updateSourceDigest(id, digest);
    return digest;
  }

  /// Checks whether [id] can be read by this step.
  ///
  /// If it's a declared output from an earlier phase, wait for it to be built.
  Future<Readability> _isReadableId(AssetId id) async {
    if (_runningBuild!.buildState.isActualPostOutput(id)) {
      // Post process outputs are not readable until after the build.
      return Readability.notReadable;
    }
    if (_runningBuild.buildState.isDeclaredOutput(id)) {
      final step = _runningBuild.buildState.stepForDeclaredOutput(id);
      if (step.phaseNumber > _runningBuildStep!.phaseNumber) {
        return Readability.notReadable;
      } else if (step.phaseNumber == _runningBuildStep.phaseNumber) {
        // allow a build step to read its outputs (contained in writtenAssets)
        final isInBuild =
            _runningBuildStep.buildPhase is InBuildPhase &&
            assetsWritten.contains(id);

        return isInBuild ? Readability.ownOutput : Readability.notReadable;
      }

      await _runningBuild.assetBuilder(id);
      return Readability.fromPreviousPhase(
        _runningBuild.buildState.isActualSuccessfulOutput(id),
      );
    }
    return Readability.fromPreviousPhase(_runningBuild.buildState.isSource(id));
  }

  void _checkInvalidInput(AssetId id) {
    if (_runningBuild == null) return;

    final package = _runningBuild.buildPackages[id.package];
    if (package == null) {
      throw PackageNotFoundException(id.package);
    }

    // The id is an invalid input if it's not part of the build.
    if (!_runningBuild.buildConfigs.isVisibleInBuild(id, package)) {
      final allowed = _runningBuild.buildConfigs.validInputsFor(package);

      throw InvalidInputException(id, allowedGlobs: allowed);
    }
  }

  /// Triggers the evaluation of the [glob] query inside the build engine.
  Future<GlobId> _evaluateGlob(String glob) async {
    final globId = GlobId(
      package: _runningBuildStep!.primaryPackage,
      glob: glob,
      phaseNumber: _runningBuildStep.phaseNumber,
    );
    await _runningBuild!.globEvaluator(globId);
    return globId;
  }

  Future<void> writeAsBytes(AssetId id, List<int> bytes) {
    assetsWritten.add(id);
    return _delegate.writeAsBytes(id, bytes);
  }

  Future<void> writeAsString(
    AssetId id,
    String contents, {
    Encoding encoding = utf8,
  }) {
    assetsWritten.add(id);
    return _delegate.writeAsString(id, contents, encoding: encoding);
  }

  @override
  Future<PhasedValue<FileContent>> readPhased(AssetId id) async {
    if (_runningBuild == null) {
      final exists = await _delegate.canRead(id);
      if (exists) {
        final content = await _delegate.readAsString(id);
        final hash = md5.convert(utf8.encode(content)).toString();
        return PhasedValue.fixed(BuildRunnerFileContent(id.path, true, content, hash));
      } else {
        return PhasedValue.fixed(BuildRunnerFileContent.missing(id.path));
      }
    }

    if (_isGpAsset(id)) {
      final primaryInput = _primaryInputForGpAsset(id);
      final partsByPhase = <int, List<String>>{};
      final steps = _runningBuild.buildState.stepResultsForPrimaryInput(primaryInput);
      if (steps != null) {
        for (final entry in steps.entries) {
          final stepResult = entry.value;
          if (stepResult.succeeded && stepResult.partsWritten.isNotEmpty) {
            partsByPhase
                .putIfAbsent(entry.key, () => [])
                .addAll(stepResult.partsWritten);
          }
        }
      }

      if (partsByPhase.isEmpty) {
        return PhasedValue<FileContent>.of(BuildRunnerFileContent.missing(id.path), expiresAfter: phase);
      }

      final sortedPhases = partsByPhase.keys.toList()..sort();
      var currentValue = '';
      
      final primaryContent = await _delegate.readAsString(primaryInput);
      final match = RegExp(r'//\s*@dart\s*=\s*[0-9.]+\b').firstMatch(primaryContent);
      final versionOverride = match != null ? '${match.group(0)}\n\n' : '';
      
      final header = versionOverride.isNotEmpty
          ? "$versionOverride\npart of '../${p.posix.basename(primaryInput.path)}';\n\n"
          : "part of '../${p.posix.basename(primaryInput.path)}';\n\n";
      var phasedValue = PhasedValue<FileContent>.of(BuildRunnerFileContent.missing(id.path), expiresAfter: sortedPhases.first);

      for (var i = 0; i < sortedPhases.length; i++) {
        final phaseNum = sortedPhases[i];
        final addedParts = partsByPhase[phaseNum]!;
        currentValue = currentValue.isEmpty
            ? addedParts.join('\n\n')
            : '$currentValue\n\n${addedParts.join('\n\n')}';

        final content = '$header$currentValue';
        final hash = md5.convert(utf8.encode(content)).toString();
        final fileContent = BuildRunnerFileContent(id.path, true, content, hash);

        final nextPhase = (i + 1 < sortedPhases.length) ? sortedPhases[i + 1] : phase;
        phasedValue = phasedValue.followedBy(ExpiringValue<FileContent>(fileContent, expiresAfter: nextPhase));
      }

      return phasedValue;
    }

    if (!_runningBuild.buildState.isFile(id)) {
      // Add to the build state for input tracking.
      _runningBuild.buildState.addMissingSource(id);
      return PhasedValue.fixed(BuildRunnerFileContent.missing(id.path));
    } else if (_runningBuild.buildState.isMissingSource(id)) {
      return PhasedValue.fixed(BuildRunnerFileContent.missing(id.path));
    }

    if (_runningBuild.buildState.isDeclaredOutput(id)) {
      final step = _runningBuild.buildState.stepForDeclaredOutput(id);
      final stepPhase = step.phaseNumber;
      if (stepPhase >= phase) {
        return PhasedValue.unavailable(
          before: BuildRunnerFileContent.missing(id.path),
          expiresAfter: stepPhase,
        );
      } else {
        // If needed, trigger a build at an earlier phase.
        if (!_runningBuild.assetIsProcessedOutput(id)) {
          await _runningBuild.assetBuilder(id);
        }
        final stepResult = _runningBuild.buildState.stepResult(step);
        final isSuccessOutput =
            _runningBuild.buildState.isActualOutput(id) && stepResult.succeeded;
        
        final content = isSuccessOutput ? await _delegate.readAsString(id) : '';
        final hash = isSuccessOutput
            ? md5.convert(utf8.encode(content)).toString()
            : md5.convert(const []).toString();
        final fileContent = BuildRunnerFileContent(id.path, isSuccessOutput, content, hash);

        return PhasedValue.generated(
          fileContent,
          atPhase: stepPhase,
          before: BuildRunnerFileContent.missing(id.path),
        );
      }
    }

    final exists = await _delegate.canRead(id);
    if (exists) {
      final content = await _delegate.readAsString(id);
      final hash = md5.convert(utf8.encode(content)).toString();
      return PhasedValue.fixed(BuildRunnerFileContent(id.path, true, content, hash));
    } else {
      return PhasedValue.fixed(BuildRunnerFileContent.missing(id.path));
    }
  }

  @override
  Future<BuildRunnerFileContent> readAtPhase(AssetId id) async {
    if (!await canRead(id, track: false)) {
      return BuildRunnerFileContent.missing(id.path);
    }

    final content = await readAsString(id, track: false);
    final hash = base64.encode((await digest(id, track: false)).bytes);
    return BuildRunnerFileContent(id.asPath, true, content, hash);
  }

  bool _isGpAsset(AssetId id) {
    if (!id.path.contains('/_gp/')) return false;
    return id.path.endsWith('.gp.dart');
  }

  AssetId _primaryInputForGpAsset(AssetId gpId) {
    final parts = p.posix.split(gpId.path);
    final topDir = parts[0];
    final subPath = parts.sublist(2).join('/');
    final remainder = subPath.substring(0, subPath.length - '.gp.dart'.length) + '.dart';
    return AssetId(gpId.package, '$topDir/$remainder');
  }

  String? _readGpContent(AssetId gpId, {required int upToPhase}) {
    if (_runningBuild == null) return null;
    final primaryInput = _primaryInputForGpAsset(gpId);
    final steps = _runningBuild.buildState.stepResultsForPrimaryInput(primaryInput);
    if (steps == null || steps.isEmpty) return null;

    final sortedPhases = steps.keys.toList()..sort();
    final accumulated = <String>[];
    for (final phaseNum in sortedPhases) {
      if (phaseNum >= upToPhase) break;
      final stepResult = steps[phaseNum]!;
      if (stepResult.succeeded && stepResult.partsWritten.isNotEmpty) {
        accumulated.addAll(stepResult.partsWritten);
      }
    }
    if (accumulated.isEmpty) return null;
    final header = "part of '../${p.posix.basename(primaryInput.path)}';\n\n";
    return '$header${accumulated.join('\n\n')}';
  }
}
