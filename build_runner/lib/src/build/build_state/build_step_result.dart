import 'package:build/build.dart' hide Builder;
import 'package:built_collection/built_collection.dart';
import 'package:built_value/built_value.dart';
import 'package:built_value/serializer.dart';

import '../asset_content.dart';
import 'glob_id.dart';

part 'build_step_result.g.dart';

/// Execution results and dependency tracking for a build step.
abstract class BuildStepResult
    implements Built<BuildStepResult, BuildStepResultBuilder> {
  static Serializer<BuildStepResult> get serializer =>
      _$buildStepResultSerializer;

  /// Whether the build step succeeded, or `null` if it did not complete or was
  /// skipped.
  bool? get result;

  /// Whether the outputs generated by this step go in the build cache.
  bool get isHidden;

  /// Outputs written by the step.
  BuiltMap<AssetId, AssetContent> get outputs;

  /// Part contribution written by the step.
  AssetContent? get partContribution;

  /// Part imports written by the step.
  AssetContent? get partImports;

  /// The exact dart language version comment (e.g. `// @dart=2.14`) found
  /// in the primary input, if this step ran a builder that emitted part 
  /// contributions.
  String? get primaryLanguageVersion;

  /// Inputs and missing sources read.
  BuiltSet<AssetId> get inputs;

  /// The globs evaluated during this step.
  BuiltSet<GlobId> get globsEvaluated;

  /// Entrypoints used for resolution with the analyzer.
  BuiltSet<AssetId> get resolverEntrypoints;

  /// Errors logged.
  BuiltList<String> get errors;

  factory BuildStepResult([void Function(BuildStepResultBuilder)? updates]) =
      _$BuildStepResult;
  BuildStepResult._();

  /// Whether the build step ran, either in the previous build or in this build.
  bool get hasRun => result != null;

  /// Whether the build step ran and succeeded, either in the previous build or
  /// in this build.
  bool get succeeded => result == true;

  /// Whether the build step ran and failed, either in the previous build or in
  /// this build.
  bool get failed => result == false;
}
